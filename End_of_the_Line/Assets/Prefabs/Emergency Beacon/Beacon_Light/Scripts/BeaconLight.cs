using UnityEngine;
using System.Collections;

public class BeaconLight : MonoBehaviour {
//--------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------
    
    GameObject reflektor;
    float angle;

//--------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------
void Awake()
{
    reflektor = transform.Find("reflektor").gameObject;
    angle = 0.0f;
}
//--------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------
void Update()
{
    angle += Time.deltaTime * 450.0f;
    reflektor.transform.localEulerAngles = new Vector3(0.0f, angle, 0.0f);
}
//--------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------
}
